const getNestedValue = (source, key) => {
	return key.split(".").reduce((value, part) => {
		if (value && Object.prototype.hasOwnProperty.call(value, part)) {
			return value[part];
		}
		return undefined;
	}, source);
};

const applyTranslations = (language) => {
	const dictionary = translations[language] || translations.es;

	document.querySelectorAll("[data-i18n]").forEach((element) => {
		const key = element.dataset.i18n;
		const value = getNestedValue(dictionary, key);
		if (value !== undefined) {
			element.textContent = value;
		}
	});

	document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
		const key = element.dataset.i18nPlaceholder;
		const value = getNestedValue(dictionary, key);
		if (value !== undefined) {
			element.setAttribute("placeholder", value);
		}
	});

	document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
		const key = element.dataset.i18nAlt;
		const value = getNestedValue(dictionary, key);
		if (value !== undefined) {
			element.setAttribute("alt", value);
		}
	});

	document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
		const key = element.dataset.i18nAriaLabel;
		const value = getNestedValue(dictionary, key);
		if (value !== undefined) {
			element.setAttribute("aria-label", value);
		}
	});

	const title = getNestedValue(dictionary, "meta.title");
	const description = getNestedValue(dictionary, "meta.description");
	if (title) {
		document.title = title;
	}
	if (description) {
		const descriptionMeta = document.querySelector('meta[name="description"]');
		if (descriptionMeta) {
			descriptionMeta.setAttribute("content", description);
		}
	}

	document.documentElement.lang = language;

	const languageButton = document.getElementById("language-toggle");
	if (languageButton) {
		languageButton.textContent = language === "es" ? "EN" : "ES";
	}
};

const initializeLanguage = () => {
	const storedLanguage = localStorage.getItem("kipu-language");
	const language = storedLanguage === "en" || storedLanguage === "es" ? storedLanguage : "es";

	applyTranslations(language);

	const languageButton = document.getElementById("language-toggle");
	if (languageButton) {
		languageButton.addEventListener("click", () => {
			const currentLanguage = document.documentElement.lang === "en" ? "en" : "es";
			const nextLanguage = currentLanguage === "es" ? "en" : "es";
			localStorage.setItem("kipu-language", nextLanguage);
			applyTranslations(nextLanguage);
		});
	}
};

const initializeMobileNav = () => {
	const navToggle = document.getElementById("nav-toggle");
	const header = document.querySelector(".main-header");
	const navLinks = document.querySelectorAll(".main-navigation .nav-list a");

	if (!navToggle || !header) {
		return;
	}

	const closeMenu = () => {
		header.classList.remove("nav-open");
		navToggle.setAttribute("aria-expanded", "false");
	};

	navToggle.addEventListener("click", () => {
		const isOpen = header.classList.toggle("nav-open");
		navToggle.setAttribute("aria-expanded", String(isOpen));
	});

	navLinks.forEach((link) => {
		link.addEventListener("click", closeMenu);
	});

	window.addEventListener("resize", () => {
		if (window.innerWidth > 1024) {
			closeMenu();
		}
	});
};

document.addEventListener("DOMContentLoaded", () => {
	if (window.lucide) {
		window.lucide.createIcons({
			attrs: {
				stroke: "currentColor",
				"stroke-width": 1.5,
				class: "lucide-icon"
			}
		});
	}

	initializeLanguage();
	initializeMobileNav();
});
